# typecasting in python
# typecasting is the process of converting one data type to another data type

# there are two types of typecasting
# 1. implicit typecasting
# 2. explicit typecasting
# implicit typecasting is done by python automatically
# explicit typecasting is done by the user manually

# implicit typecasting example
a = 5
b= 10.5
print("a+b is",a+b)

# explicit typecasting example
a= 5
b= 10.5
print("a+b is", a + int(b))  # converting float to int

print("a+b is", a + float(b))  # converting int to float